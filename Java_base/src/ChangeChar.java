public class ChangeChar {

    public static void main(String[] args) {

        // 指表符\t
        System.out.println("北京\t上海\t武汉");
       
        // 换行符\n
        System.out.println("你好~la~\n再见~");

        // 输出\, ', "
        System.out.println("C:\\Users\\zhouyiran\\Desktop");
        System.out.println("林总说:\"你要好好学习\"");
        System.out.println("老吴说:\"你要好好向\'林总\'学习\"");
        // 回车符\r
        System.out.println("周总牛逼！\r林总");
        // 课堂作业
        System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
    }

}
